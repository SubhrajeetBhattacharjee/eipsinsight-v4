export function getUpgradeBlogs(slug: string): BlogPost[] {
  switch (slug.toLowerCase()) {
    case 'frontier':
      return frontierPosts;
    case 'homestead':
      return homesteadPosts;
    case 'dao-fork':
      return daoForkPosts;
    case 'tangerine-whistle':
      return tangerineWhistlePosts;
    case 'spurious-dragon':
      return spuriousDragonPosts;
    case 'byzantium':
      return byzantiumPosts;
    case 'constantinople':
      return constantinoplePosts;
    case 'istanbul':
      return istanbulPosts;
    case 'berlin':
      return berlinPosts;
    case 'london':
      return londonPosts;
    case 'paris':
      return mergePosts;
    case 'shanghai':
      return shanghaiPosts;
    case 'cancun':
      return cancunPosts;
    case 'pectra':
      return pectraPosts;
    case 'fusaka':
      return fusakaPosts;
    case 'glamsterdam':
      return glamsterdamPosts;
    default:
      return [];
  }
}
